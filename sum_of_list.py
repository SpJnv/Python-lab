def sum_of_list(lst):
    """
    This function takes a list of numbers as input
    and returns the sum of all the numbers in the list.
    """
    return sum(lst)

# Example usage:
my_list = [1, 2, 3, 4, 5]  # Define a list of numbers
total = sum_of_list(my_list)  # Call the function to calculate the sum
print("The sum of the list is:", total)  # Print the result

#Output
"""
The sum of the list is: 15

"""
