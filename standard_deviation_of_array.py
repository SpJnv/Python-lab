# importing the numpy package
import numpy as np

# Given data
arr = np.array([20, 2, 7, 1, 34])

# find the mean of the array
mean_arr = np.mean(arr)

# printing the mean of array
print(f"mean of the array = {mean_arr}")

# calculating the standard deviation
standard_dev = np.std(arr)

# printing the standard deviation
print(f"standard deviation of the array = {standard_dev}")


# Output
"""
mean of the array = 12.8
standard deviation of the array = 12.576167937809991
"""
